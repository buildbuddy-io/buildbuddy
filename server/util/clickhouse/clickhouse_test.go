// TestExecutionIDRoundTripThroughClickHouseSchema verifies that an execution
// ID generated by the server via digest.NewCASResourceName().NewUploadString()
// can be reconstructed from the split columns populated on schema.Execution.
func TestExecutionIDRoundTripThroughClickHouseSchema(t *testing.T) {
	const sha256Hash = "072d9dd55aacaa829d7d1cc9ec8c4b5180ef49acac4a3c2f3ca16a3db134982d"

	for _, tc := range []struct {
		name           string
		instanceName   string
		digestFunction repb.DigestFunction_Value
		compressor     repb.Compressor_Value
		sizeBytes      int64
	}{
		{name: "OldStyleSha256", instanceName: "instance_name", digestFunction: repb.DigestFunction_SHA256, compressor: repb.Compressor_IDENTITY, sizeBytes: 1234},
		{name: "Blake3", instanceName: "instance_name", digestFunction: repb.DigestFunction_BLAKE3, compressor: repb.Compressor_IDENTITY, sizeBytes: 9876},
		{name: "Sha256Zstd", instanceName: "instance_name", digestFunction: repb.DigestFunction_SHA256, compressor: repb.Compressor_ZSTD, sizeBytes: 4321},
		{name: "Blake3Zstd", instanceName: "instance_name", digestFunction: repb.DigestFunction_BLAKE3, compressor: repb.Compressor_ZSTD, sizeBytes: 55},
		{name: "EmptyInstanceName", instanceName: "", digestFunction: repb.DigestFunction_SHA256, compressor: repb.Compressor_IDENTITY, sizeBytes: 7},
		{name: "InstanceNameContainingUploads", instanceName: "bad/uploads/instance", digestFunction: repb.DigestFunction_SHA256, compressor: repb.Compressor_IDENTITY, sizeBytes: 42},
		// DEFLATE is unsupported by URL construction/parsing, so it is treated
		// as IDENTITY: NewUploadString emits a "blobs" URL and the parser
		// recovers Compressor_IDENTITY. The round-trip still matches.
		{name: "DeflateUnsupported", instanceName: "instance_name", digestFunction: repb.DigestFunction_SHA256, compressor: repb.Compressor_DEFLATE, sizeBytes: 99},
	} {
		t.Run(tc.name, func(t *testing.T) {
			rn := digest.NewCASResourceName(
				&repb.Digest{Hash: sha256Hash, SizeBytes: tc.sizeBytes},
				tc.instanceName,
				tc.digestFunction,
			)
			rn.SetCompressor(tc.compressor)
			executionID := rn.NewUploadString()

			execution, err := clickhouse.ExecutionFromProto(&repb.StoredExecution{ExecutionId: executionID}, nil)
			require.NoError(t, err)

			require.Equal(t, executionID, reconstructExecutionID(t, execution))
		})
	}
}

func TestExecutionFromProto(t *testing.T) {
	executionID := digest.NewCASResourceName(
		&repb.Digest{
			Hash:      "072d9dd55aacaa829d7d1cc9ec8c4b5180ef49acac4a3c2f3ca16a3db134982d",
			SizeBytes: 1234,
		},
		"test-instance-name",
		repb.DigestFunction_SHA256,
	).NewUploadString()

	for _, testCase := range []struct {
		name string
		in   *repb.StoredExecution
		want *schema.Execution
	}{
		{
			name: "MinimalExecution",
			in: &repb.StoredExecution{
				ExecutionId: executionID,
			},
			want: &schema.Execution{},
		},
		{
			name: "RecycleRunner",
			in: &repb.StoredExecution{
				ExecutionId:   executionID,
				RecycleRunner: true,
			},
			want: &schema.Execution{
				RecycleRunner: true,
			},
		},
		{
			name: "TestMetadata",
			in: &repb.StoredExecution{
				ExecutionId:     executionID,
				TestSize:        "large",
				TestShardIndex:  2,
				TestTotalShards: 6,
			},
			want: &schema.Execution{
				TestSize:        "large",
				TestShardIndex:  2,
				TestTotalShards: 6,
			},
		},
	} {
		t.Run(testCase.name, func(t *testing.T) {
			// Convert the stored execution as the ClickHouse flush path does.
			execution, err := clickhouse.ExecutionFromProto(testCase.in, nil)
			require.NoError(t, err)

			// The row should preserve direct proto fields while also parsing
			// the execution ID resource fields.
			require.Equal(t, testCase.want.RecycleRunner, execution.RecycleRunner)
			require.Equal(t, testCase.want.TestSize, execution.TestSize)
			require.Equal(t, testCase.want.TestShardIndex, execution.TestShardIndex)
			require.Equal(t, testCase.want.TestTotalShards, execution.TestTotalShards)
			require.Equal(t, testCase.in.GetExecutionId(), reconstructExecutionID(t, execution))
		})
	}
}

func reconstructExecutionID(t *testing.T, e *schema.Execution) string {
	d := &repb.Digest{
		Hash:      hex.EncodeToString([]byte(e.ActionDigestHash)),
		SizeBytes: int64(e.ActionDigestSize),
	}
	df, err := digest.DigestFunctionFromSegment(e.DigestFunction, d)
	require.NoError(t, err)
	compressor, err := digest.CompressorFromSegment(e.Compressor)
	require.NoError(t, err)
	rn := digest.NewCASResourceName(d, e.InstanceName, df)
	rn.SetCompressor(compressor)
	return rn.UploadString(e.ExecutionUUID)
}

func TestFlushExecutionStats_SkipsMalformedExecutionIDs(t *testing.T) {
	flags.Set(t, "testenv.use_clickhouse", true)
	flags.Set(t, "testenv.reuse_server", true)

	ctx := context.Background()
	env := testenv.GetTestEnv(t)

	invocationID := "6ec99c6d-5504-4934-9f31-a9b65af4ec76"
	invocationUUID := strings.ReplaceAll(invocationID, "-", "")
	invocation := &sipb.StoredInvocation{InvocationId: invocationID}
	nowUsec := time.Now().UnixMicro()

	validExecutionID := digest.NewCASResourceName(
		&repb.Digest{
			Hash:      "072d9dd55aacaa829d7d1cc9ec8c4b5180ef49acac4a3c2f3ca16a3db134982d",
			SizeBytes: 1234,
		},
		"test-instance-name",
		repb.DigestFunction_SHA256,
	).NewUploadString()

	err := env.GetOLAPDBHandle().FlushExecutionStats(ctx, invocation, []*repb.StoredExecution{
		{
			ExecutionId:     validExecutionID,
			InvocationUuid:  invocationUUID,
			UpdatedAtUsec:   nowUsec,
			CreatedAtUsec:   nowUsec,
			TestSize:        "enormous",
			TestShardIndex:  3,
			TestTotalShards: 8,
		},
		{
			ExecutionId:    "not-a-resource-name",
			InvocationUuid: invocationUUID,
			UpdatedAtUsec:  nowUsec,
			CreatedAtUsec:  nowUsec,
		},
		{
			// Empty execution ID is also rejected.
			ExecutionId:    "",
			InvocationUuid: invocationUUID,
			UpdatedAtUsec:  nowUsec,
			CreatedAtUsec:  nowUsec,
		},
	})
	require.NoError(t, err)

	var rows []schema.Execution
	err = env.GetOLAPDBHandle().GORM(ctx, "test_query_executions").Where(
		"invocation_uuid = ?", invocationUUID,
	).Find(&rows).Error
	require.NoError(t, err)
	require.Len(t, rows, 1)
	require.Equal(t, validExecutionID, reconstructExecutionID(t, &rows[0]))
	require.Equal(t, "enormous", rows[0].TestSize)
	require.Equal(t, uint32(3), rows[0].TestShardIndex)
	require.Equal(t, uint32(8), rows[0].TestTotalShards)
}

func TestFlushExecutionStats_AllMalformedExecutionIDs_SkipsInsert(t *testing.T) {
	flags.Set(t, "testenv.use_clickhouse", true)
	flags.Set(t, "testenv.reuse_server", true)

	ctx := context.Background()
	env := testenv.GetTestEnv(t)

	invocationID := "7118a8ef-f598-4e83-9953-a3b4b799d3ab"
	invocationUUID := strings.ReplaceAll(invocationID, "-", "")
	invocation := &sipb.StoredInvocation{InvocationId: invocationID}
	nowUsec := time.Now().UnixMicro()

	err := env.GetOLAPDBHandle().FlushExecutionStats(ctx, invocation, []*repb.StoredExecution{
		{
			ExecutionId:    "bad-1",
			InvocationUuid: invocationUUID,
			UpdatedAtUsec:  nowUsec,
			CreatedAtUsec:  nowUsec,
		},
		{
			ExecutionId:    "bad-2",
			InvocationUuid: invocationUUID,
			UpdatedAtUsec:  nowUsec,
			CreatedAtUsec:  nowUsec,
		},
	})
	require.NoError(t, err)

	var count int64
	err = env.GetOLAPDBHandle().GORM(ctx, "test_count_executions").Model(&schema.Execution{}).Where(
		"invocation_uuid = ?", invocationUUID,
	).Count(&count).Error
	require.NoError(t, err)
	require.Zero(t, count)
}
